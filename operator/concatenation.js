//concatenation :joining or merging 2 or more literals
// concatenation can be achieved by using + or , 
// + operator can perform concatenation and addition also
// number+number =addition
// number+bool=addition
// number+string=concat
//bool+bool=addition
//bool+string=concat
//string+string=concat
document.write(10+10);
document.write("<br>");
document.write(10+"20");
console.log(100+true); //101
console.log(true+false); //1
console.log(100+false); 
console.log(true+"false"); //truefalse
console.log(10+20+true);
console.log(true+"false"+10+20); //truefalse1020
console.log(20+20+"hello"+"30");
console.log("hello"+"world");
console.log("hello"+20);
console.log(true+false+"true"+"false"+20+30); //truefalse2030
console.log("true"+"false"+10+20+true);


